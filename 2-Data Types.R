#DATA TYPES IN R

#There are five main data categories in R. These are:
  #numeric: 1.5, 55, 432 etc
  #integer: 1L, 36L, 1123L
    #Note: L declares the number as integer.
  #complex: 5+3i
  #character/string: "Mehmet", "k", "2241"
  #logical/boolean: TRUE or FALSE

#We can use the class() function to check a variable's type:

num1=4
class(num1)
#[1] "numeric"
